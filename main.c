    //当num&1==1 说明最低位肯定是一
    //0000000000000000000000000000011
    //0000000000000000000000000000001
    //0000000000000000000000000000001 按位与1 拿到最低位就是1
    int num = 0;
    scanf("%d",&num);
    int count = 0;
    for(int i = 0; i<32;i++)
    {

        //当前向右移动 i为  就可以分别判断 每一个是什么数字
        if(((num>>i) &1) ==1){
            count ++;
        }
    }
    printf("%d",count);
