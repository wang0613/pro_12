int main1() {

    int a = 16;
    int b = a >> 1; //>> -右移操作符  移动的是为二进制 10000-->01000
    //右移操作符：
    //1:算术右移
    //右边丢弃，左边补原符号位(正负)
    //2:逻辑右移
    //右边丢弃，左边补0

    printf("b = %d\n", b); //8  0100
    //整数的二进制表示有：原码 反码 补码
    //存储到内存中的是补码，打印的是原码
    int n = -1;
    //1000000000000000000001  原码
    //1111111111111111111110  反码 符号位不变
    //1111111111111111111111  补码

    n >> 1; //向右移动一位 如果是算数右移 补符号位1
    //1111111111111111111111  补码不变所以打印的还是-1


    //对于左移来说 左边丢弃 右边补0
    int y = 5;
    int x = y << 1;  //101->1010
    printf("%d\n", x);



    return 0;
}

